public class PER11no2 {

    public static void main(String[] args) {
        
             int tinggi = 10;
           
             for (int i = 1; i <= tinggi; i++) {
              for (int j = 1; j <= tinggi - i; j++) {
               System.out.print(" ");
              }
              for (int k = 1; k <= i; k++) {
                System.out.print("*");
            }
              System.out.println();
             }
            }
           }
    
